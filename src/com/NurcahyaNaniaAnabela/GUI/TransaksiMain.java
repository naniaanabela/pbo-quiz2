    //method untuk menambahkan id
    private void incId(){
        this.id += 1;
    }
    
    //method untuk melakukan pengurangan id
    private void decId(){
        this.id -= 1;
    }
    
    //setter untuk melakukan setKode 
    private String setKode(){
        this.incId();
        //tanggal
        String set = new SimpleDateFormat("yyMMdd").format(new Date());
        this.code = String.format(set + "%02d" , this.id);
        return code;
    }
    
    private Object[] addItem (String nama , int jumlah) {
        float harga = 0 ;
        TransaksiCombo items = new TransaksiCombo();
        for (int i = 0; i < items.getNama().size(); i++) {
            if (nama.equalsIgnoreCase(items.getNama().get(i))) {
                harga = items.getHarga().get(i) ;
            }
        }
        Object[] obj = {
            nama ,
            harga ,
            jumlah ,
        } ;
        return obj ;
    }
    
    //method untuk melakukan update pada jumlah
    private void updateJumlah (String nama , int add) {
        ArrayList<String> item = new ArrayList<> () ;
        for (int i = 0; i < tbModel.getRowCount(); i++) {
            item.add (tbModel.getValueAt (i , 0).toString()) ;
        }
        for (int i = 0; i < item.size(); i++) {
            if (item.get(i).equals(nama)) {
                int jumlah = new Integer (tbModel.getValueAt (i , 2).toString()) ;
                tbModel.setValueAt (jumlah+add , i , 2) ;
            }
        }
    }
    
    // method untuk melakukan pengecekan jika terdapat item yang double
    private boolean isDuplicate (String nama) {
        boolean result = false ;
        ArrayList <String> item = new ArrayList<>() ;
        for (int i = 0; i < tbModel.getRowCount(); i++) {
            item.add (tbModel.getValueAt(i, 0).toString()) ;
        }
        for (String i : item) {
            if (i.equals(nama)) {
                result = true ;
            }
        }
        
        return result ;
    }
    
    //method untuk melakukan pengecekan apakah table kosong
